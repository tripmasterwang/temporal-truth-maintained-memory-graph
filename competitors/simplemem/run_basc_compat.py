"""Faithful SimpleMem driver.

Wraps `competitors/simplemem/simplemem/main.py:SimpleMemSystem`. SimpleMem's
default embedder (Qwen3-Embedding-0.6B) is not locally available; their
`utils/embedding.py` falls back to `sentence-transformers/all-MiniLM-L6-v2`
when the primary model fails to load, so we run with the fallback to avoid
a 1.2GB download on a tight-disk machine.

Session-id traceback
--------------------
SimpleMem's `add_dialogue(speaker, content, timestamp)` stores `timestamp`
on the resulting MemoryEntry. We encode each input session as a unique
ISO date `2025-01-DDTHH:MM:SS` where DD = (1 + session_idx mod 28). On
retrieval we read entry.timestamp and parse DD back to session_idx.

Budget enforcement: post_truncate by entry timestamp recency / topic.
"""
