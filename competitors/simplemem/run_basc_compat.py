def _split_dialogue(text: str) -> list[tuple[str, str]]:
    """Return list of (speaker, content) pairs from a session text blob."""
    out: list[tuple[str, str]] = []
    cur_speaker: str | None = None
    cur_buf: list[str] = []
    for line in text.splitlines():
        l = line.strip()
        if l.startswith("user:"):
            if cur_speaker:
                out.append((cur_speaker, " ".join(cur_buf).strip()))
            cur_speaker = "user"; cur_buf = [l[5:].strip()]
        elif l.startswith("assistant:"):
            if cur_speaker:
                out.append((cur_speaker, " ".join(cur_buf).strip()))
            cur_speaker = "assistant"; cur_buf = [l[10:].strip()]
        elif l.startswith("speaker_a:"):
            if cur_speaker:
                out.append((cur_speaker, " ".join(cur_buf).strip()))
            cur_speaker = "speaker_a"; cur_buf = [l[10:].strip()]
        elif l.startswith("speaker_b:"):
            if cur_speaker:
                out.append((cur_speaker, " ".join(cur_buf).strip()))
            cur_speaker = "speaker_b"; cur_buf = [l[10:].strip()]
        else:
            cur_buf.append(l)
    if cur_speaker:
        out.append((cur_speaker, " ".join(cur_buf).strip()))
    return out or [("user", text[:8000])]


def _ts_for_idx(i: int) -> str:
    """Encode session_idx into an ISO-8601 date that survives normalisation."""
    day = 1 + (i % 28)
    return f"2025-01-{day:02d}T00:00:00"


def _idx_from_ts(ts: str) -> int | None:
    if not isinstance(ts, str) or not ts.startswith("2025-01-"):
        return None
    try:
        day = int(ts[8:10])
    except ValueError:
        return None
    return day - 1 if 1 <= day <= 28 else None


def consolidate(memory: list[Item], *, budget_tokens: int,
                reader_model: str | None = None,
                seed: int = 7,
                llm_model: str = "deepseek-v3.2") -> ConsolidatedBuffer:
    del reader_model
    _ensure_config(llm_model=llm_model)

    tmp = Path(tempfile.mkdtemp(prefix=f"simplemem_basc_{seed}_"))
    db_path = tmp / "lancedb"
    db_path.mkdir(parents=True, exist_ok=True)

    try:
        from main import SimpleMemSystem  # noqa: WPS433
    except Exception as e:
        return ConsolidatedBuffer(
            retained_text=["" for _ in memory],
            retrievable_mask=[False for _ in memory],
            total_tokens=0,
            budget_enforcement="import-failed",
            notes=dict(error=str(e)[:200]),
        )

    try:
        sys_ = SimpleMemSystem(
            api_key=os.environ.get("MAAS_API_KEY", ""),
            model=llm_model,
            base_url="https://api.modelarts-maas.com/openai/v1",
            db_path=str(db_path),
            table_name=f"basc_{seed}",
            clear_db=True,
            enable_planning=False,
            enable_reflection=False,
            enable_parallel_processing=False,
        )
    except Exception as e:
        shutil.rmtree(tmp, ignore_errors=True)
        return ConsolidatedBuffer(
            retained_text=["" for _ in memory],
            retrievable_mask=[False for _ in memory],
            total_tokens=0,
            budget_enforcement="instantiation-failed",
            notes=dict(error=str(e)[:200]),
        )

    # Ingest each session's turns with our timestamp-encoded session_idx.
    for i, it in enumerate(memory):
        ts = _ts_for_idx(i)
        for spk, content in _split_dialogue(it.text):
            if not content:
                continue
            try:
                sys_.add_dialogue(speaker=spk, content=content[:4000], timestamp=ts)
            except Exception:
                pass

    # Walk vector store entries → recover session_idx provenance.
    n = len(memory)
    retained_per_idx: dict[int, list[str]] = {}
    importance: list[float] = [0.0] * n
    try:
        # SimpleMem's vector_store exposes a list of MemoryEntry-like dicts.
        all_entries = sys_.vector_store.get_all() if hasattr(sys_.vector_store, "get_all") else []
        for ent in all_entries:
            ts = ent.get("timestamp") if isinstance(ent, dict) else getattr(ent, "timestamp", None)
            idx = _idx_from_ts(ts)
            if idx is None or idx >= n:
                continue
            txt = ent.get("lossless_restatement") if isinstance(ent, dict) else getattr(ent, "lossless_restatement", "")
            if txt:
                retained_per_idx.setdefault(idx, []).append(txt)
                importance[idx] = max(importance[idx], 1.0)
    except Exception:
        pass

    retained_text = [" ".join(retained_per_idx.get(i, [])) for i in range(n)]
    n_kept_tokens = [len(t.split()) for t in retained_text]
    retrievable_mask = [bool(t.strip()) for t in retained_text]
    cur = sum(n_kept_tokens)
    enforcement = "native"
    if cur > budget_tokens:
        retained_text, retrievable_mask, cur = post_truncate_to_budget(
            retained_text, retrievable_mask, n_kept_tokens, importance,
            budget_tokens,
        )
        enforcement = "post-truncate"

    shutil.rmtree(tmp, ignore_errors=True)
    return ConsolidatedBuffer(
        retained_text=retained_text,
        retrievable_mask=retrievable_mask,
        total_tokens=cur,
        budget_enforcement=enforcement,
        notes=dict(mapped=int(sum(retrievable_mask))),
    )


def _smoke() -> int:
    memory = [
        Item(session_id=f"s{i}",
             text=("user: I went to Paris. assistant: Lovely. " * 2),
             n_tokens=40 + i * 5)
        for i in range(3)
    ]
    raw = sum(it.n_tokens for it in memory)
    cap = int(raw * 1.0)
    t0 = time.time()
    buf = consolidate(memory, budget_tokens=cap, llm_model="deepseek-v3.2")
    print(f"SimpleMem smoke kept={sum(buf.retrievable_mask)}/{len(memory)} "
          f"total={buf.total_tokens} enforce={buf.budget_enforcement} dt={time.time()-t0:.1f}s")
    return 0


if __name__ == "__main__":
    raise SystemExit(_smoke())
